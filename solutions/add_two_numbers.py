def add_numbers(a: int, b: int) -> int:
    """Adds two numbers and returns their sum.

    Parameters:
    a(int): The first integer.
    b(int): The second integer.add()

    Returns:
    int: The sum of the two integers.add()

    Examples:
    >>> add_numbers(1, 2)
    3
    """
    return a + b
